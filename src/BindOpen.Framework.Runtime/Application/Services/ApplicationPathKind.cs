namespace BindOpen.Framework.Runtime.Application.Services
{
    /// <summary>
    /// This enumeration lists all the possible kinds of application path.
    /// </summary>
    public enum ApplicationPathKind
    {
        /// <summary>
        /// The application folder.
        /// </summary>
        AppFolder,

        /// <summary>
        /// The default log folder.
        /// </summary>
        DefaultLogFolder,

        /// <summary>
        /// The extensions folder.
        /// </summary>
        ExtensionsFolder,

        /// <summary>
        /// The library folder.
        /// </summary>
        LibraryFolder,

        /// <summary>
        /// The log folder.
        /// </summary>
        LogFolder,

        /// <summary>
        /// The roaming folder.
        /// </summary>
        RoamingFolder,

        /// <summary>
        /// The runtime folder.
        /// </summary>
        RuntimeFolder,

        /// <summary>
        /// The settings file.
        /// </summary>
        SettingsFile,

        /// <summary>
        /// The settings folder.
        /// </summary>
        SettingsFolder,

        /// <summary>
        /// The temporary folder.
        /// </summary>
        TemporaryFolder,

        /// <summary>
        /// The users folder.
        /// </summary>
        UsersFolder,
    }
}